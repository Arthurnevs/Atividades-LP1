s0 = float(input('Posição inicial? '))
v0 = float(input('Velocidade inicial? '))
t = float(input('Tempo? '))
acel = float(input('Aceleração? '))

v = v0 + acel * t

s = s0 + v0 * t + (acel * (t ** 2))/2

vm = (s - s0)/t

print('')
print('Dados da questão')
print('================')
print('   Posição inicial: {:.2f} m'.format(float(s0)))
print('Velocidade inicial: {:.2f} m/s'.format(float(v0)))
print('        Aceleração: {:.2f} m/s2'.format(float(acel)))
print('             Tempo: {:.2f} s'.format(float(t)))
print('  Velocidade final: {:.2f} m/s'.format(float(v)))
print('  Velocidade média: {:.2f} m/s'.format(float(vm)))
print('     Posição final: {:.2f} m'.format(float(s)))
